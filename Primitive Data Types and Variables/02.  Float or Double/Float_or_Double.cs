using System;

class Float_or_Double
{
    static void Main(string[] args)
    {
        float value1 = 3456.091f;
        float value2 = 12.345f;
        double value3 = 34.567839023;
        double value4 = 8923.1234857;
        Console.WriteLine(" Type float: \n {0} = 3456.091 \n {1} = 12.345", value1, value2);
        Console.WriteLine("\n Type double: \n {0} = 34.567839023 \n {1} = 8923.1234857 ", value3, value4);
    }
}
